def add(arg1, arg2):
    """
    This function add tkaes 2 arguments
    arg1 and arg2
    It returns the addition of arg1 and arg2
    """
    return arg1+arg2

def sub(arg1, arg2):
    """

    Parameters
    ----------
    arg1 : a number
        first argument
    arg2 : a number
        second argument

    Returns
    -------
    TYPE
        DESCRIPTION.

    """
    return arg1-arg2


